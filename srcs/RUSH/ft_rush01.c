#include "ft_putchar.c"

void	ft_rush(int x, int y)
{
	int	i;
	
	i = 1;
	while (i <= y)
	{
		if (i == 1)
			ft_print(x, '/', '*', '\\');
		else if (i <= (y - 1))
			ft_print(x, '*', ' ', '*');
		if (i == y)
			ft_print(x, '\\', '*', '/');
		ft_putchar('\n');
		i++;
	}
}
